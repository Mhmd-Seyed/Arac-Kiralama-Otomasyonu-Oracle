CREATE TABLE ARACLAR (
    ARAC_ID NUMBER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    MARKA VARCHAR2(50),
    MODEL VARCHAR2(50),
    YIL NUMBER(4,0),
    PLAKA VARCHAR2(20),
    GUNLUK_UCRET NUMBER(10,2),
    DURUM VARCHAR2(10) DEFAULT 'BOS'
);

CREATE TABLE BAKIM_GECMISI (
    BAKIM_ID NUMBER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    ARAC_ID NUMBER,
    GIRIS_TARIHI DATE,
    MALIYET NUMBER(10,2),
    ISLEM_ACIKLAMASI VARCHAR2(200),
    CONSTRAINT FK_BAKIM_ARAC FOREIGN KEY (ARAC_ID) REFERENCES ARACLAR (ARAC_ID)
);

CREATE TABLE ODEMELER (
    ODEME_ID NUMBER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    KIRALAMA_ID NUMBER,
    ODEME_TARIHI DATE DEFAULT SYSDATE,
    TUTAR NUMBER(10,2),
    ODEME_TURU VARCHAR2(20),
    CONSTRAINT FK_ODEME_KIRA FOREIGN KEY (KIRALAMA_ID) REFERENCES KIRALAMALAR (KIRALAMA_ID)
);

CREATE TABLE MUSTERILER (
    MUSTERI_ID NUMBER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    AD_SOYAD VARCHAR2(100),
    TC_NO VARCHAR2(11),
    TELEFON VARCHAR2(15)
);

CREATE TABLE KIRALAMALAR (
    KIRALAMA_ID NUMBER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    MUSTERI_ID NUMBER,
    ARAC_ID NUMBER,
    ALIS_TARIHI DATE DEFAULT SYSDATE,
    TESLIM_TARIHI DATE,
    TOPLAM_UCRET NUMBER(10,2),
    CONSTRAINT FK_MUSTERI FOREIGN KEY (MUSTERI_ID) REFERENCES MUSTERILER (MUSTERI_ID),
    CONSTRAINT FK_ARAC FOREIGN KEY (ARAC_ID) REFERENCES ARACLAR (ARAC_ID)
);

